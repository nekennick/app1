String getResultName(int number) {
  switch (number) {
    case 1:
      return '"Xin chúc mừng! Tình yêu của bạn rất là đẹp đó nhé. Tình cảm của 2 bạn khá mặn nồng, khiến không ít người ngưỡng mộ và cảm phục đấy. Người ta thường nói là duyên phận ý trời mà, có tránh cũng sẽ không tránh được đâu nhá. Bạn nên đón nhận tình cảm thật của mình đi và đừng có trốn tránh nữa nhé."';

    case 2:
      return '"Số 2 nói với bạn rằng trái tim mách bảo tình yêu nhưng con tim lại không thể làm chủ được lý trí của mình. Mặc dù yêu nhau nồng thắm nhưng các bạn lại không có lý trí, không thể vượt qua được những định kiến, sự cấm cản của gia đình."';

    case 3:
      return '"Các bạn có một tình bạn rất là đẹp và trong sáng, thuần khiết một tình yêu mà bao nhiêu người phải ngưỡng mộ. Xin chúc mừng nhé."';

    case 4:
      return '"Hai bạn cũng có một chuyện tình vô cùng đẹp mà nhiều người ao ước lắm đó. Hãy thầm cảm ơn ông tơ bà nguyệt đã se duyên đi nào."';

    case 5:
      return '"Có vẻ như 2 bạn chỉ nên dừng lại ở mức độ tình bạn thôi. Đừng quá buồn nhé bởi tình yêu đích thực của cuộc đời bạn đang trên đường tìm đến với bạn đấy. Có thể do tắc đường nên sẽ đến chậm một chút thôi mà."';

    case 6:
      return '"Có vẻ 2 bạn là định mệnh của đời nhau đúng không? Yêu nhau từ cái nhìn đầu tiên à? Xin chúc mừng nhé, 2 bạn rất hợp nhau đấy."';

    case 7:
      return '"Xin chúc mừng, 2 bạn sinh ra là để thuộc về nhau đấy. Hai bạn rất hợp nhau cả về suy nghĩ lẫn sở thích, hành động. Hãy biết trân trọng tình cảm đẹp này và cùng người ấy vun đắp để nó mãi bền vững bạn nhé."';

    case 8:
      return '"Bạn không phải là một con người kén chọn trong tình yêu, tình yêu đến, bạn sẽ biết cách bắt lấy nó kịp thời và bạn cũng đang rất hạnh phúc với tình yêu này đúng không? Chúc bạn và một nửa sẽ luôn yêu thương nhau như bây giờ nhé."';

    case 9:
      return '"Bạn không phải là một con người kén chọn trong tình yêu, tình yêu đến, bạn sẽ biết cách bắt lấy nó kịp thời và bạn cũng đang rất hạnh phúc với tình yêu này đúng không? Chúc bạn và một nửa sẽ luôn yêu thương nhau như bây giờ nhé."';

    default:
      return '"Không hợp lệ"';
  }
}
